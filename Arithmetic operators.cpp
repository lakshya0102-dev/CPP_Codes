#include<iostream>
using namespace std;
int main(){
    int a = 10, b = 5;
    cout << "First number: " << a << endl;
    cout << "Second number: " << b << endl;

    cout << "\nAddition: " << a + b << endl;
    cout << "\nSubtraction: " << a - b << endl;
    cout << "\nMultiplication: " << a * b << endl;
    cout << "\nDivision: " << a / b << endl;
    cout << "\nModulus: " << a % b << endl;

    return 0;
}